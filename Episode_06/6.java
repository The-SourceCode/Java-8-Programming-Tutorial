public class Java8 {
    public static void main(String args[]) {
        double candybar = 2.50;
        double apple = 1.15;
        double money = 1.15;

        //Equal to ==
        //Not Equal to !=
        //Less than <
        //Less than or equal to <=
        //Greater than >
        //Greater than or equal to >=
        
        if (money != apple) {
            System.out.println("You can not purchase the apple!");
        }
        if (money != candybar) {
            System.out.println("You can not purchase the candybar!");
        }
    }
}